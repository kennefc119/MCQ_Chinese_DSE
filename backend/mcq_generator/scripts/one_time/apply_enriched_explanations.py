def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--apply", action="store_true")
    args = parser.parse_args()

    print(f"Enriched explanations prepared: {len(ENRICHED)}")

    # Verify all IDs exist in DB
    print("\nVerifying option IDs...")
    ok_count = 0
    missing = []
    for opt_id in ENRICHED:
        resp = sb.table("dsemcq_question_options").select("id, explanation").eq("id", opt_id).execute()
        if resp.data:
            ok_count += 1
        else:
            missing.append(opt_id)

    print(f"  Found: {ok_count}, Missing: {len(missing)}")
    if missing:
        print(f"  Missing IDs: {missing[:10]}...")

    if not args.apply:
        # Preview some
        print(f"\nPREVIEW (first 10):")
        for i, (opt_id, new_expl) in enumerate(list(ENRICHED.items())[:10]):
            resp = sb.table("dsemcq_question_options").select("explanation").eq("id", opt_id).execute()
            old = (resp.data[0]["explanation"] or "").strip() if resp.data else "?"
            print(f"\n  {opt_id}")
            print(f"    OLD: {old}")
            print(f"    NEW: {new_expl[:80]}...")
        print(f"\n** DRY RUN — {ok_count} updates ready. Re-run with --apply. **")
        return

    # Apply
    print(f"\nApplying {ok_count} updates...")
    ok = 0
    fail = 0
    for opt_id, new_expl in ENRICHED.items():
        try:
            sb.table("dsemcq_question_options").update(
                {"explanation": new_expl}
            ).eq("id", opt_id).execute()
            ok += 1
        except Exception as e:
            print(f"  FAILED {opt_id}: {e}")
            fail += 1

    print(f"Done. {ok} updated, {fail} failed.")


if __name__ == "__main__":
    main()
